import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const catalog = JSON.parse(readFileSync(join(root, 'data/skill-catalog.json'), 'utf8'));
const rules = JSON.parse(readFileSync(join(root, 'data/skill-unlock-rules.json'), 'utf8'));
const outputPath = join(root, 'apps/academia-lendaria-ads-studio/src/generated/skill-catalog.ts');
const briefSchema = JSON.parse(readFileSync(join(root, 'data/project-brief.schema.json'), 'utf8'));
const briefSchemaOutputPath = join(root, 'apps/academia-lendaria-ads-studio/src/generated/project-brief-schema.ts');

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  [
    '// Generated by scripts/generate-skill-catalog.mjs. Do not edit manually.',
    `export const skillCatalog = ${JSON.stringify(catalog, null, 2)} as const;`,
    `export const skillUnlockRules = ${JSON.stringify(rules, null, 2)} as const;`,
    '',
  ].join('\n'),
);

console.log(`Generated ${outputPath} with ${catalog.skills.length} skills.`);

writeFileSync(
  briefSchemaOutputPath,
  [
    '// Generated by scripts/generate-skill-catalog.mjs. Do not edit manually.',
    `export const projectBriefSchema = ${JSON.stringify(briefSchema, null, 2)} as const;`,
    '',
  ].join('\n'),
);

console.log(`Generated ${briefSchemaOutputPath}.`);
